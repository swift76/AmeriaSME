create or alter procedure sp_SaveEcosystemOwner(
	@APPLICATION_ID			uniqueidentifier,
	@COUNTRY				char(2),
	@DISTRICT				nvarchar(20),
	@COMMUNITY				nvarchar(40),
	@STREET					nvarchar(100),
	@BUILDING				nvarchar(40),
	@APARTMENT				nvarchar(40),
	@CURRENT_COUNTRY		char(2),
	@CURRENT_DISTRICT		nvarchar(20),
	@CURRENT_COMMUNITY		nvarchar(40),
	@CURRENT_STREET			nvarchar(100),
	@CURRENT_BUILDING		nvarchar(40),
	@CURRENT_APARTMENT		nvarchar(40),
	@PASSPORT_TYPE			char(1),
	@PASSPORT_NUMBER		varchar(20),
	@PASSPORT_DATE			date,
	@PASSPORT_EXPIRY_DATE	date,
	@PASSPORT_BY			varchar(10),
	@SOCIAL_CARD_NUMBER		char(10),
	@BIRTH_DATE				date,
	@GENDER					bit,
	@CITIZENSHIP_CODE		char(2),
	@FIRST_NAME				nvarchar(60),
	@LAST_NAME				nvarchar(60),
	@PATRONYMIC_NAME		nvarchar(60),
	@FIRST_NAME_EN			varchar(60),
	@LAST_NAME_EN			varchar(60),
	@SHARE_INTEREST			money
)
AS
	insert into ECOSYSTEM_OWNER (
		APPLICATION_ID,
		COUNTRY,
		DISTRICT,
		COMMUNITY,
		STREET,
		BUILDING,
		APARTMENT,
		CURRENT_COUNTRY,
		CURRENT_DISTRICT,
		CURRENT_COMMUNITY,
		CURRENT_STREET,
		CURRENT_BUILDING,
		CURRENT_APARTMENT,
		PASSPORT_TYPE,
		PASSPORT_NUMBER,
		PASSPORT_DATE,
		PASSPORT_EXPIRY_DATE,
		PASSPORT_BY,
		SOCIAL_CARD_NUMBER,
		BIRTH_DATE,
		GENDER,
		CITIZENSHIP_CODE,
		FIRST_NAME,
		LAST_NAME,
		PATRONYMIC_NAME,
		FIRST_NAME_EN,
		LAST_NAME_EN,
		SHARE_INTEREST
	)
	values (
		@APPLICATION_ID,
		@COUNTRY,
		@DISTRICT,
		@COMMUNITY,
		@STREET,
		@BUILDING,
		@APARTMENT,
		@CURRENT_COUNTRY,
		@CURRENT_DISTRICT,
		@CURRENT_COMMUNITY,
		@CURRENT_STREET,
		@CURRENT_BUILDING,
		@CURRENT_APARTMENT,
		@PASSPORT_TYPE,
		@PASSPORT_NUMBER,
		@PASSPORT_DATE,
		@PASSPORT_EXPIRY_DATE,
		@PASSPORT_BY,
		@SOCIAL_CARD_NUMBER,
		@BIRTH_DATE,
		@GENDER,
		@CITIZENSHIP_CODE,
		@FIRST_NAME,
		@LAST_NAME,
		@PATRONYMIC_NAME,
		@FIRST_NAME_EN,
		@LAST_NAME_EN,
		@SHARE_INTEREST
	)
GO
